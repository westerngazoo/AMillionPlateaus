// ── The recording mock `ctx` ────────────────────────────────────────────────
// Records method calls and style-property sets as plain-data ops. Imported by
// nothing in `src` — a local test helper only. `canvas` is read for clearRect.
export function recordingCtx({ width = 900, height = 600 } = {}) {
  const log = [];
  const op = (...parts) => log.push(parts);
  const ctx = {
    canvas: { width, height },
    __log: log,
    clearRect: (...a) => op("clearRect", ...a),
    save: () => op("save"),
    restore: () => op("restore"),
    beginPath: () => op("beginPath"),
    moveTo: (...a) => op("moveTo", ...a),
    lineTo: (...a) => op("lineTo", ...a),
    arc: (...a) => op("arc", ...a),
    stroke: () => op("stroke"),
    fill: () => op("fill"),
    fillText: (...a) => op("fillText", ...a),
    setLineDash: (dash) => op("setLineDash", [...dash]),
  };
  for (const prop of [
    "strokeStyle",
    "fillStyle",
    "lineWidth",
    "globalAlpha",
    "font",
    "textAlign",
  ]) {
    let value;
    Object.defineProperty(ctx, prop, {
      get: () => value,
      set: (next) => {
        value = next;
        op("set " + prop, next);
      },
    });
  }
  return ctx;
}

