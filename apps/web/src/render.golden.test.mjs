// The same placement `render.js` computed internally, so the seam draws over an
// identical geometry (project → spreadNodes).
export function placeFixture() {
  const raw = new Map();
  for (const p of FIXTURE.plateaus) raw.set(p.id, project(p.position, VIEW));
  return spreadNodes(raw);
}

// Capture the op-log a drawing function produces against the mock ctx.
export function captureLog(drawInto) {
  const ctx = recordingCtx({ width: 900, height: 600 });
  drawInto(ctx);
  return ctx.__log;
}

// Drive the extracted seam directly through its PUBLIC API — viewModel builds the
// Frame, canvasRenderer replays it — over the same placement render.js computes.
export function captureSeam(state) {
  return captureLog((ctx) => {
    const positions = placeFixture();
    const frame = viewModel({ ...FIXTURE }, positions, state);
    const canvas = { width: 900, height: 600, getContext: () => ctx };
    canvasRenderer(canvas).draw(frame);
  });
}

